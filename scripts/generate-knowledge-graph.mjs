#!/usr/bin/env node
// Static-analysis pass over src/ that builds a file-level import graph and
// renders it as both a JSON artifact and a human-readable architecture doc.
// No network access, no external dependencies, no LLM calls.

import { readFileSync, readdirSync, statSync, writeFileSync } from 'fs'
import { dirname, join, relative, resolve } from 'path'
import { fileURLToPath } from 'url'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const SRC = join(ROOT, 'src')
const SOURCE_EXTS = ['.ts', '.tsx', '.js', '.jsx']
const IMPORT_RE = /(?:import\s+(?:[^'"]+?\s+from\s+)?|export\s+[^'"]*?\s+from\s+|require\()\s*['"]([^'"]+)['"]/g

function listSourceFiles(dir) {
  const out = []
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name)
    if (entry.isDirectory()) {
      out.push(...listSourceFiles(full))
    } else if (SOURCE_EXTS.some((ext) => entry.name.endsWith(ext))) {
      out.push(full)
    }
  }
  return out
}

// Imports in this codebase are written as relative ESM specifiers ending in
// `.js` even though the actual source files are `.ts`/`.tsx` (a TS convention
// for ESM output). Resolve by trying the literal path, then swapping in each
// known source extension, then falling back to an `index.*` inside it.
function resolveImport(fromFile, specifier) {
  if (!specifier.startsWith('.')) return null // skip bare/package imports
  const base = resolve(dirname(fromFile), specifier)
  const candidates = [base, base.replace(/\.js$/, '')]
  for (const candidate of candidates) {
    for (const ext of SOURCE_EXTS) {
      try {
        if (statSync(candidate + ext).isFile()) return candidate + ext
      } catch {}
    }
    for (const ext of SOURCE_EXTS) {
      try {
        if (statSync(join(candidate, 'index' + ext)).isFile())
          return join(candidate, 'index' + ext)
      } catch {}
    }
  }
  return null
}

function topDir(relPath) {
  const parts = relPath.split('/')
  return parts.length > 1 ? parts[0] : '(root)'
}

const files = listSourceFiles(SRC)
const nodes = []
const edges = []
const fanIn = new Map()

for (const file of files) {
  const relPath = relative(ROOT, file)
  const content = readFileSync(file, 'utf8')
  const imports = []
  for (const match of content.matchAll(IMPORT_RE)) {
    const resolved = resolveImport(file, match[1])
    if (resolved) imports.push(relative(ROOT, resolved))
  }
  nodes.push({
    id: relPath,
    dir: topDir(relative(SRC, file)),
    lines: content.split('\n').length,
    bytes: Buffer.byteLength(content),
  })
  for (const target of imports) {
    edges.push({ from: relPath, to: target })
    fanIn.set(target, (fanIn.get(target) || 0) + 1)
  }
}

const graph = { generatedAt: new Date().toISOString(), nodeCount: nodes.length, edgeCount: edges.length, nodes, edges }
writeFileSync(join(ROOT, 'knowledge-graph.json'), JSON.stringify(graph, null, 2) + '\n')

// --- Directory-level rollup (for a readable Mermaid diagram) ---
const dirEdgeCounts = new Map()
for (const { from, to } of edges) {
  const a = topDir(relative(SRC, resolve(ROOT, from)))
  const b = topDir(relative(SRC, resolve(ROOT, to)))
  if (a === b) continue
  const key = `${a}|${b}`
  dirEdgeCounts.set(key, (dirEdgeCounts.get(key) || 0) + 1)
}

// --- Tool catalog: pull the real DESCRIPTION/name out of each tool's source ---
const toolsDir = join(SRC, 'tools')
const tools = []
for (const entry of readdirSync(toolsDir, { withFileTypes: true })) {
  if (!entry.isDirectory()) continue
  const promptFile = join(toolsDir, entry.name, 'prompt.ts')
  let description = null
  try {
    const promptSrc = readFileSync(promptFile, 'utf8')
    const m = promptSrc.match(/export const DESCRIPTION\s*=\s*(?:`([^`]*)`|'([^']*)'|"([^"]*)")/)
    const raw = m && (m[1] ?? m[2] ?? m[3])
    if (raw) description = raw.trim().split('\n')[0]
  } catch {}
  tools.push({ dir: entry.name, description })
}
tools.sort((a, b) => a.dir.localeCompare(b.dir))

// --- Fan-in leaderboard: which modules are depended on the most ---
const topFanIn = [...fanIn.entries()].sort((a, b) => b[1] - a[1]).slice(0, 15)

const dirCounts = new Map()
for (const n of nodes) dirCounts.set(n.dir, (dirCounts.get(n.dir) || 0) + 1)

const lines = []
lines.push('# Architecture Overview')
lines.push('')
lines.push(
  '_Generated by [`scripts/generate-knowledge-graph.mjs`](scripts/generate-knowledge-graph.mjs) via static analysis of `src/` — no LLM involved, regenerate any time with `node scripts/generate-knowledge-graph.mjs`._'
)
lines.push('')
lines.push(`This pass scanned **${nodes.length} files** and resolved **${edges.length} local import edges** under \`src/\`.`)
lines.push('')
lines.push('## Top-level modules')
lines.push('')
lines.push('| Directory | Files | Role |')
lines.push('| --- | --- | --- |')
const roleByDir = {
  entrypoints: 'CLI bootstrap (`cli.tsx`) and the MCP server entrypoint (`mcp.ts`).',
  screens: 'Full-screen Ink views (REPL, Doctor, log list, resume picker, npm-prefix setup).',
  commands: 'Implementations of `/slash` commands (config, cost, init, login, review, ...).',
  components: 'Reusable Ink/React components shared across screens and commands.',
  tools: 'Agent tool implementations (Bash, file read/edit/write, grep, glob, agent, ...).',
  services: 'External integrations: Claude API client, MCP client, OAuth, Sentry, Statsig.',
  hooks: 'React hooks for terminal/input state (text input, key history, timers, ...).',
  utils: 'Shared utilities: config, git, logging, permissions, shell, markdown, etc.',
  constants: 'Static strings/config: prompts, product name, ASCII art, feature betas.',
}
for (const [dir, count] of [...dirCounts.entries()].sort((a, b) => b[1] - a[1])) {
  lines.push(`| \`src/${dir}\` | ${count} | ${roleByDir[dir] || '—'} |`)
}
lines.push('')
lines.push('## Directory dependency graph')
lines.push('')
lines.push('Edges below are aggregated from file-level imports (self-loops within a directory omitted); thicker textual weight = more underlying import edges.')
lines.push('')
lines.push('```mermaid')
lines.push('flowchart LR')
const sortedDirEdges = [...dirEdgeCounts.entries()].sort((a, b) => b[1] - a[1])
for (const [key, count] of sortedDirEdges) {
  const [a, b] = key.split('|')
  lines.push(`  ${a} -->|${count}| ${b}`)
}
lines.push('```')
lines.push('')
lines.push('## Most-depended-on modules (fan-in)')
lines.push('')
lines.push('Files imported from the largest number of other files — a rough proxy for "core" modules worth understanding first.')
lines.push('')
lines.push('| File | Imported by N files |')
lines.push('| --- | --- |')
for (const [file, count] of topFanIn) {
  lines.push(`| \`${file}\` | ${count} |`)
}
lines.push('')
lines.push('## Tool catalog')
lines.push('')
lines.push('Each entry under `src/tools/` is a self-contained agent tool. Descriptions are the first line of each tool\'s own `DESCRIPTION` constant (the same text the model sees).')
lines.push('')
lines.push('| Tool | Description |')
lines.push('| --- | --- |')
for (const t of tools) {
  lines.push(`| \`${t.dir}\` | ${t.description ? t.description.replace(/\|/g, '\\|') : '—'} |`)
}
lines.push('')
lines.push('## Regenerating')
lines.push('')
lines.push('```bash')
lines.push('node scripts/generate-knowledge-graph.mjs')
lines.push('```')
lines.push('')
lines.push('This rewrites `knowledge-graph.json` (full file-level node/edge list) and this document.')
lines.push('')

writeFileSync(join(ROOT, 'ARCHITECTURE.md'), lines.join('\n'))

console.log(`Wrote knowledge-graph.json (${nodes.length} nodes, ${edges.length} edges) and ARCHITECTURE.md`)
